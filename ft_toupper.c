int	ft_toupper(int x)
{
	char	c;

	c = ((char)x);
	if (c >= 'a' && c <= 'z' )
		c = c - 32;
	else if (c >= 'A' && c <= 'Z')
		c = c + 32;
	return ((int)c);
}